public class Tugas4{

    public static void main(String []args){
        int ang = 1;
        int hasil = Penjumlahan(ang);
        System.out.println("Penjumlahan " + ang + " + 2 = " + hasil);
     }
     
    public static int Penjumlahan(int x) {
        int angka = x;
        
        return angka + 2; 
   }
}